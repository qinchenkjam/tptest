	$("#formsbmit").click(function () {

	var user = {};

	user.user_name=$("#username").val();

	user.password= $("#password").val();

	user.email= $("#email").val();

	

	$.ajax({

	type: "POST",

	url: "<?php echo url('admin/Admin/save'); ?>",

	data: user,

	dataType: 'json',

	success: function (data) {

	if (data.status == -1) {

	$(".tis").html(data.msg);

	   return false;

	} else {

	location.href="<?php echo url('admin/admin/index'); ?>";;

	return true;

	}

	}

	})

	});

});
